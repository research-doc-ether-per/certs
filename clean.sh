
① ベースイメージ最小化

slim 版のベースイメージを使い、バージョンを固定することで、
不要なパッケージを減らし、セキュリティリスクを抑えることができます。

② 最小特権ユーザー

専用ユーザーで実行することで、
万一問題が発生した場合の影響範囲を小さく抑えることができます。

③ イメージ署名・検証（運用側）

イメージの署名と検証を運用側で行うことで、
改ざんやなりすましのリスクを抑えることができます。

④ 依存関係管理

依存関係を lockfile に基づいて固定することで、
意図しない更新による問題を防ぐことができます。

⑤ 秘密情報をイメージに含めない

秘密情報をイメージに含めない運用にすることで、
情報漏洩のリスクを抑えることができます。

⑥ タグ管理（latest を使わない／運用側）

latest を使わないタグ運用にすることで、
実行中のイメージが分からなくなる問題を防ぐことができます。

⑦ COPY の使用

ADD ではなく COPY に統一することで、
イメージの中身が分かりにくくなる問題を防ぐことができます。

⑧ メタデータ付与

イメージにラベルを付与することで、
サービスの識別ミスや運用上の混乱を防ぐことができます。

⑨ マルチステージビルド（使い分け）

マルチステージビルドを必要な箇所に限定することで、
不要な構成を含めないようにし、リスクを抑えることができます。

⑩ exec 形式で起動

exec 形式で直接起動することで、
停止時のシグナル処理を安定させ、運用上の問題を防ぐことができます。
